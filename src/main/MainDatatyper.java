package main;

public class MainDatatyper {

	// starter prog her
	public static void main(String[] args) {
		System.out.println("Prog kjører");
		// Primitive datatyper
		// heltall
		byte denneVariabelenInneholderEnByte = 42;
		short denneVariabelenInneholderEnShort = 23000;
		int denneVariabelenInneholderEnInt = 761834184;
		long denneVariabelenInneholderEnLong = 7618341841L;
		
		// desimaltall
		float denneVariabelenInneholderEnFloat = 42.33333333F;
		double denneVariabelenInneholderEnDouble = 42.1764518;
		
		// boolean
		boolean denneVariabelenInneholderEnBolskVerdi = true;
		
		//Text - som enkelt tegn
		char denneVariabelenInneholderEnChar = 'A';
		
		char P = 'P';
		char e = 'e';
		char r = 'r';
		
		System.out.println(P + "" + e + "" + r);
		
		// streng bestående av char
		String tekst = "Hei hopp spis sopp";
		System.out.println(tekst);
		
	}

}
